USE QTBDV
CREATE TABLE LE_TAN
(
	MALT CHAR(4) CONSTRAINT PK_LT PRIMARY KEY,
	CONSTRAINT CK_MALT CHECK (MALT LIKE 'LT__'),
	TENLT NVARCHAR(100) NOT NULL,
	SDT CHAR(10) CONSTRAINT CHK_SDT CHECK (LEN(SDT)=10),
	NGAYSINH DATETIME CONSTRAINT CHK_NGSINH CHECK (DATEDIFF(YEAR, NGAYSINH, GETDATE())>=18),
	DIACHI NVARCHAR(100) NOT NULL
)
CREATE TABLE PHONG
(
	MAPHG CHAR(3) CONSTRAINT PK_P PRIMARY KEY,
	SOPHONG CHAR(1) CONSTRAINT CHK_SP CHECK(SOPHONG>=1),
	SOGIUONG CHAR(1) CONSTRAINT CHK_SG CHECK(SOGIUONG>=1 AND SOGIUONG<=4)
)
CREATE TABLE DICH_VU
(
	MADV VARCHAR(10) CONSTRAINT PK_DV PRIMARY KEY,
	TENDV NVARCHAR(100) NOT NULL,
	THOILUONG INT NOT NULL CONSTRAINT CHK_TLUONG CHECK(THOILUONG>=30 AND THOILUONG<=120),
	DONGIA MONEY NOT NULL CONSTRAINT CHK_DONGIA CHECK(DONGIA>0)
)
CREATE TABLE PTTHANHTOAN
(
	MATHANHTOAN CHAR(4) CONSTRAINT PK_PTTT PRIMARY KEY,
	CONSTRAINT CK_MTT CHECK (MATHANHTOAN LIKE 'TT__'),
	PHUONGTHUC NVARCHAR(50) NOT NULL,
	CONSTRAINT CK_PTHUC CHECK(PHUONGTHUC IN ('CASH', 'CARD','BANK', 'APPLE PAY','KLOOK'))
)
CREATE TABLE KHACH_HANG
(
	MAKH VARCHAR(100) CONSTRAINT PK_KH PRIMARY KEY,
	TENKH NVARCHAR(100) NOT NULL,
	MAGIAMGIA VARCHAR(10),
	PTDATHEN NVARCHAR(100) CONSTRAINT CK_PTDATHEN CHECK (PTDATHEN IN ('WHATSAPP', 'LINE', 'ZALO', 'FB', 'KAKAO', 'WECHAT', 'WALK', 'ĐỐI TÁC')),
	LIENHE TEXT
)
CREATE TABLE HOA_DON
(
	MAHD VARCHAR(100) CONSTRAINT PK_HD PRIMARY KEY,
	TONGPHU MONEY CONSTRAINT CHK_TONGPHU CHECK(TONGPHU>0),
	TONG MONEY CONSTRAINT CHK_TONG CHECK(TONG>=0),
	NGAYNHAP DATETIME NOT NULL CONSTRAINT CHK_NGAYGIO CHECK ((GETDATE()>=NGAYNHAP)),
	MALT CHAR(4) NOT NULL CONSTRAINT FK_HD_LT FOREIGN KEY(MALT) REFERENCES LE_TAN(MALT), 
	MAKH VARCHAR(100) NOT NULL CONSTRAINT FK_HD_KH FOREIGN KEY(MAKH) REFERENCES KHACH_HANG(MAKH)
)
CREATE TABLE CTHD
(
	MAHD VARCHAR(100) NOT NULL,
	MADV VARCHAR(10) NOT NULL,
	MAPHG CHAR(3) NOT NULL,
	CHIETKHAU FLOAT,
	THANHTIEN MONEY CONSTRAINT CHK_TTIEN CHECK(THANHTIEN>=0),
	CONSTRAINT PK_CTHD PRIMARY KEY(MAHD, MADV, MAPHG),
	CONSTRAINT FK_HD_CTHD FOREIGN KEY(MAHD) REFERENCES HOA_DON(MAHD),
	CONSTRAINT FK_DV_CTHD FOREIGN KEY(MADV) REFERENCES DICH_VU(MADV),
	CONSTRAINT FK_P_CTHD FOREIGN KEY(MAPHG) REFERENCES PHONG(MAPHG)
)
CREATE TABLE CTTT
(
	MAHD VARCHAR(100) NOT NULL,
	MATHANHTOAN CHAR(4) NOT NULL,
	SOTIEN MONEY CONSTRAINT CHK_SOTIEN CHECK(SOTIEN>0),
	CONSTRAINT PK_CTTT PRIMARY KEY(MAHD, MATHANHTOAN),
	CONSTRAINT FK_HD_CTTT FOREIGN KEY(MAHD) REFERENCES HOA_DON(MAHD),
	CONSTRAINT FK_PTTT_CTTT FOREIGN KEY(MATHANHTOAN) REFERENCES PTTHANHTOAN(MATHANHTOAN)
)
