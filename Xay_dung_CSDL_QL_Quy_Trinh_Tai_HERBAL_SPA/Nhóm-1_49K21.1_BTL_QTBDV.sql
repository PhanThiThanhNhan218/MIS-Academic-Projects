-- CẬP NHẬT CHIETKHAU CỦA BẢNG CTHD
UPDATE CTHD
SET CTHD.CHIETKHAU = CASE 
     WHEN KH.MAGIAMGIA LIKE 'DIS%' 
     THEN CAST(SUBSTRING(KH.MAGIAMGIA, 4, LEN(KH.MAGIAMGIA) - 3) AS INT)
     ELSE 0 
END
FROM CTHD
JOIN HOA_DON HD ON HD.MAHD = CTHD.MAHD
JOIN KHACH_HANG KH ON KH.MAKH = HD.MAKH
-- CẬP NHẬT THANHTIEN CỦA BẢNG CTHD
UPDATE CTHD SET THANHTIEN = CASE
	WHEN CHIETKHAU =0 THEN DONGIA
	WHEN CHIETKHAU >0 THEN DONGIA*(1-(CHIETKHAU/100))
END
FROM CTHD
JOIN DICH_VU DV ON DV.MADV=CTHD.MADV
JOIN HOA_DON HD ON HD.MAHD=CTHD.MAHD
-- CẬP NHẬT TONGPHU CỦA BẢNG HOA_DON
UPDATE HD
SET HD.TONGPHU = (
					SELECT SUM(DV.DONGIA)
					FROM CTHD
					JOIN DICH_VU DV ON CTHD.MADV = DV.MADV
					WHERE CTHD.MAHD = HD.MAHD)
FROM HOA_DON HD
-- CẬP NHẬT TONG CỦA BẢNG HOA_DON
UPDATE HD
SET HD.TONG = (
				SELECT SUM(CTHD.THANHTIEN)
				FROM CTHD
				JOIN DICH_VU DV ON CTHD.MADV = DV.MADV
				WHERE CTHD.MAHD = HD.MAHD)
FROM HOA_DON HD
-- CẬP NHẬT SOPHONG TRONG BẢNG PHONG
UPDATE PHONG
SET SOPHONG = SUBSTRING(MAPHG, 1, 1)
-- CẬP NHẬT SOGIUONG TRONG BẢNG PHONG
UPDATE PHONG
SET SOGIUONG = SUBSTRING(MAPHG, 3, 1)
--CẬP NHẬT SOTIEN CHO BẢNG CTTT
UPDATE CTTT
SET CTTT.SOTIEN = HD.TONG
FROM HOA_DON HD
JOIN CTTT ON CTTT.MAHD = HD.MAHD
WHERE (
    SELECT COUNT(*)
    FROM CTTT
    WHERE CTTT.MAHD = HD.MAHD
) = 1